package gpms.senary.bean;

public class Tweet {	
	private int id;
	private String text;
	private String date;
	private String user;
	private double lat;
	private double lng;
	private int km100;
	private int km50;
	private int km25;
	private int km20;
	private int km10;
	private int km05;

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	public int getKm100() {
		return km100;
	}
	public void setKm100(int km100) {
		this.km100 = km100;
	}
	public int getKm50() {
		return km50;
	}
	public void setKm50(int km50) {
		this.km50 = km50;
	}
	public int getKm25() {
		return km25;
	}
	public void setKm25(int km25) {
		this.km25 = km25;
	}
	public int getKm20() {
		return km20;
	}
	public void setKm20(int km20) {
		this.km20 = km20;
	}
	public int getKm10() {
		return km10;
	}
	public void setKm10(int km10) {
		this.km10 = km10;
	}
	public int getKm05() {
		return km05;
	}
	public void setKm05(int km05) {
		this.km05 = km05;
	}
	
	
	
}
